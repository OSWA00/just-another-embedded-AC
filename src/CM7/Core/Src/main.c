void vPrintLCD(void *pvParameters)
{
  const TickType_t xDelay = 700 / portTICK_PERIOD_MS;
  int received = 0; 
  char buffer[2];
  for (;;)
  {
    xQueueReceive(queue1, &received, portMAX_DELAY);
    lcd_put_cur(0, 0);
    lcd_send_string("Hello World");
    lcd_put_cur(1,0);
    snprintf(buffer, 2, "%d", received);
    lcd_send_string(buffer);
    vTaskDelay( xDelay );
  }
}

